# Напишіть програму looppy.py, яка виведе на екран усі числа від 1 до 100 в яких цифри однакові,
# приклад чисел: 11, 22, 99, всі числа з одною цифрою(1-9) потрібно вивести також.
# Числа виводити у порядку спадання, від більшого до меншого. Останнім числом має бути 1.
f = []
for i in range(1, 100):
    num = str(i)
    if len(num) == 1 or num[0] == num[1]:
        f.append(num)
print(*reversed(f))

